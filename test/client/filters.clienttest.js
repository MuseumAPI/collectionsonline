module.exports = {
  'Filtering on search page': function (browser) {
    browser
      .url('http://localhost:8000/search?q=charles')
      .waitForElementVisible('body', 1000)
      .click('.searchtab:nth-of-type(2)')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50')
      .waitForElementVisible('.filter__box[value=artist]', 3000)
      .click('.filter__box[value=artist]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50&filter%5Boccupation%5D=artist')
      .assert.containsText('.resultcard', 'Charles Dixon')
      .click('.filter__box')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50')
      .assert.containsText('.resultcard', 'prince')
      .pause(1000)
      .click('.filter__box[value=inventor]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50&filter%5Boccupation%5D=inventor')
      .click('.filter__box[value=inventor]')
      .pause(1000)
      .click('.filter__box[value="England, United Kingdom"]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50&filter%5Bbirth%5Bplace%5D%5D=England%2C%20United%20Kingdom')
      .click('.filter__box[value="England, United Kingdom"]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50')
      .click('.filter__box[value="England, United Kingdom"]')
      .pause(1000)
      .click('.filter__box[value=chemist]')
      .pause(1000)
      .assert.urlEquals('http://localhost:8000/search/people?q=charles&page%5Bsize%5D=50&filter%5Bbirth%5Bplace%5D%5D=England%2C%20United%20Kingdom&filter%5Boccupation%5D=chemist')
      .end();
  }
};
